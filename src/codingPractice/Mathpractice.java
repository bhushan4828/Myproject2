package codingPractice;

public class Mathpractice {
	
	public static void main(String[] args) {
	
		System.out.println(Math.max(20, 30));
		System.out.println(Math.min(50, 10));
		System.out.println(Math.sqrt(64));
		System.out.println(Math.abs(-45));  //it returns the absolute positive value
		System.out.println(Math.random());  //it returns the random number between 0.0 and  1.0
		System.out.println();
		 
		int x=(int)(Math.random()*101); //to get more control over the random number
		System.out.println(x);
        
		int y=(int)(Math.random()*50);
		System.out.println(y);

		
		
}
	
}
